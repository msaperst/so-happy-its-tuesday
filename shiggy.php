										others, called "hares," who have left a trail of paper scraps
										along their route across fields, hedges, streams, bogs, and
										hills. One of the earliest Hares and Hounds events on record
										was the "Crick Run" at Rugby School in Warwickshire, England,
										first held in 1837.</font>
