									<font face="Arial"><strong>Words From a Founder</strong></font>
								</p>
								<font face="Georgia"><br> From the Kuala Lumpur Hash House
									Harriers 1500th Run Pamphlet, June 23rd 1973 </font>
								<p>
									<br> The Hash House Harriers were founded
										in a moment of post-prandial inspiration at the Selangor Club
										Chambers, about 1937/38, by the inmates, who included myself;
										E.J. Galvin, Malay Mail; H.M. Doig (H&amp;C - killed in an air
										crash just before the Japanese War); and A.S. Gispert of Evatt
										&amp; Co. Gispert was the real founder - a man of great wit
										and charm, who was killed only just returned from leave in
										Australia to rejoin the Volunteers. I am glad of this
										opportunity to salute his memory. He was a splendid fellow,
										and would be happy to know the Harriers are still going
										strong, and are as merry and bright as ever - or more so.
										Gispert was not an athlete, and stress was laid as much on the
										subsequent refreshment, etc., as on the pure and austere
										running. It was non-competitive, and abounded in slow-packs.
										Life was then conservative rather than competitive. <br> <br>
										The name was a mock allusion to the institution that housed
										and fed us. Later, Torch Bennett returned from leave, and
										produced order out of chaos - a bank account, balance sheet,
										and some system. But we prided ourselves on being rather
										disorganised - or the minimum organisation sufficed. The
										original joint maters were myself and &quot;Horse&quot;
										Thompson, still running somewhere - a past-master at
										short-cuts and the conservation of energy. <br> <br>
										Celebrations were held in various places, and the first was in
										what is now the Legislative Council, then the Volunteer Mess.
										The oratory, I recall, was much the same as now. Lew Davidson
										is an old member. Morris Edgar was one, but apart from Lew and
										John Wyatt-Smith I do not think there are any more
										ante-diluvians still running. Philip Wickens was also one who
										kept us going post-war. <br> <br> We started up again after
										the War due to Torch Bennett, who discovered a Bank Balance
										and put in a claim for War Damage on one tin bath, and two
										dozen mugs, and possibly two old bags (not members). We
										started by a small run in reduced circumstances round the
										race-course - then the horses were not much better. <br> <br>
										The Emergency cramped our style but did not diminish our
										activities, and we were even called in for information on
										various by-ways in Selangor, but our period of usefulness to
										MI 5 was brief, and our information probably otiose. But the
										hares ran into two bandits at Cheras, who were later copped. <br>
										<br> An Irish Accountant, Kennedy, drew up the Rules when we
										had to register as a Club, and he seems to have preserved the
										old traditions just as you do now. <br> <br> Cecil H. Lee<br>
										Selamat Tinggal HHH<br> Kuala Lumpur<br> 24th October 1958 <br>
										&nbsp;
								</p>

								<p align="center">
									<font face="Arial"><strong>Mystique of the Hash</strong></font>
								</p>
								<p>What is it about hashing that casts its
									spell over us and feeds our addiction? Hashing is, after all, a
									weird aberration in the world of recreational running. I raise
									the question, rhetorically, because curious people ask about
									it. They don't understand what it is that makes mature adults
									participate in an activity where the downsides and hazards seem
									so obvious. They wonder why we seem to abuse our bodies and
									risk our necks by running through inhospitable terrain.
									Thickets clogged with thorns poised to shred the skin of our
									legs. Venues burgeoning with poison ivy, ticks, bees, and
									whatever creepy-crawly thing that may be endemic to a
									particular corner of the world. Why do we choose to run up and
									down steep and often slippery trails filled with rocks and
									roots? What sadistic impulse drives the hares to lay trails
									that make us climb over and through Cyclone fences and barbed
									wire, slosh through streams and swampland rich in
									sneaker-sucking mud? What defects in our collective character
									allow us to trespass on farmland, private estates, golf
									courses, or cause security guards to get their undies in a
									bunch when we saunter through shopping malls? Why do we court
									disaster by feeling our way through dark, water-filled culverts
									and tootling along stretches of railroad tracks? People wonder
									how men and women . . . especially men . . . can reconcile
									themselves to a degree of shamelessness that allows them to
									participate in a huge slice of lunacy called a &quot;Red Dress
									Run.&quot; Who were the warped minds that conjured up an event
									in which free spirits reign supreme and the masses, garishly
									decked out in red dresses, careen around crowded business
									districts in cities throughout the world yelling inanities like
									&quot;On, On!&quot; or just screeching like banshees. And if
									we're really lucky, all of this neat stuff might go on during a
									thunderstorm, a blizzard, or in the dark of night.
									</p>
									<p>Our favored response is to tell these perplexed mortals,
										&quot;We're a drinking club with a running problem!&quot; It
										really is a pretty cute rejoinder that often elicits a broad
										smile and maybe a shout of approval . . . but it's also
										misleading and it doesn't always turn out to be the perfect
										little snappy remark we want it to be. To some onlookers it is
										a validation of their moral indignation. There's something
										scandalous about a bizarre behavior being displayed by a bunch
										of freaky people. People so deficient in basic &quot;family
										values&quot; that they let themselves get caught up in a wave
										of mob psychology which makes it okay to be seen as offensive
										or infantile. The misleading part is the inherent suggestion
										that hashers are a bunch of boozers. Not so. Emphatically, not
										so! One of the wonderful things about hashers is their
										unstated and unspoken resolve to never put pressure on anyone
										to use alcoholic beverages. It is clearly understood that some
										people prefer not to use alcohol. They don't like it, or
										health considerations rule it out. These hashers are accorded
										total respect. We don't even joke about it. Sometimes our
										response to anal-retentive mentalities derives from the
										oft-stated conviction that says, &quot;If you're talking to a
										hasher, you don't need to explain your addiction. If you're
										talking to a non-hasher, you can't explain it.&quot; I don't
										think so. We find easy comfort in this answer, but it's a
										little too trite. Too smug. It's a cop-out. I think we can
										give people answers that will make sense to them. Some of them
										may even decide to give hashing a try.</p>

									<p>
										Here then is one hasher's attempt to gather some bits and
										pieces of what may eventually evolve into a definitive
										apologetic. There is no rationale for the order of the topics
										as presented. No progression from trivial to powerful---just a
										bunch of reasons supporting the Hash House Harrier mystique in
										a way that may help outsiders get a better notion of what it's
										all about. It's not enough to say that it's all about fun and
										fitness. A lot of running clubs feature that slogan and, in a
										sense, it says it all and it makes an important statement to
										the effect that our passion is not necessarily related to
										competition, winning, or ego inflation. Paradoxically,
										however, it says nothing at all until we get into some details
										to explain what we mean by &quot;fun&quot; and how do we
										experience a <em>joie de vivre</em> you just can't get with
										treadmills and barbells. Oh, yes, the fitness gym has its
										place, but while the &quot;no pain, no gain&quot; mentality
										arguably sucks, it does work and it requires no explanation.
										Hashing, by contrast, is an alternative to the world of
										grunting, stinking, sweating bodies holed up in a jungle of
										steel contraptions. Hashing leads to a different level of
										fitness that contributes to the soundness of body in a less
										aggressive way. We can replace the grimace with a smile as the
										hashers' way to fitness takes us into an environment filled
										with the sounds, sights, and smells of nature.
									</p>
									<p>Men and women of all ages regularly savor the joy of a group
										activity that takes us bounding over trails through forests,
										along (and often into) ponds and rivers. over high meadows,
										and even over patches of asphalt. The variety of locales is
										wonderful, too. Hardly ever do we run old trails in familiar
										places. The noncompetitive aspect of hashing is a joyful
										release from the oval track, stop watches, and finishing
										chutes of the good old 10 K road race. Of course we find fun
										and camaraderie at the road races, too. Lots of hashers are
										avid road racers and there is no reason for hashing and road
										racing to be an either/or choice. The road race has its
										rewards: another T-shirt (like you only have 250 and you need
										more), medals, trophies, adulation (if you're an
										&quot;elite&quot; runner), or just a huge sense of
										accomplishment if you've covered the whole 26 miles of a
										marathon.</p>

									<p>
										Hash rewards, however, include a higher level of camaraderie
										that can only exist among close friends. Mutually shared
										expressions of warmth and affection doled out with hugs and
										smiles that extend naturally beyond the hash event. We enjoy
										getting together for non-running social events, too.
										Periodically, hashers will gather for a trip to the seashore
										or the mountains, a dinner together at an exotic restaurant,
										an entertainment event like a ballgame or a concert, or maybe
										just meeting together for a happy hour at a local brewpub and
										celebrate someone's birthday. Our occasional T-shirt is
										usually an item we buy to celebrate a special hash event, like
										a red dress tun. The only tangible award we can offer is a mug
										of beer to the &quot;winners&quot; of a hash. Toward that end,
										we bring a characteristically perverse humor by presenting our
										&quot;awards&quot; to the hares who volunteered to host the
										hash and lay the trail, short-cutters, front-runners,
										visitors, and new hashers (referred to as
										&quot;Cherries&quot;, &quot;Virgins&quot; or &quot;New
										Boots&quot;). The &quot;award ceremony&quot; is like nothing
										else. The eating and drinking climax to our physical exertions
										is called an &quot;<em>apres</em>&quot; in which our
										designated &quot;religious advisor,&quot; striving for a high
										level of refreshing irreverence, leads us in the singing of
										appropriately raunchy tunes.
									</p>

									<p>
										The variety of personalities that constitute a regional hash
										are quite amazing. And it's not only the individual
										hashers---the hash, as an entity, is likely to have a
										personality. Some hash units are more party oriented rather
										than being gung-ho for running. In a light-hearted way, they
										display their mock disdain for hard running by using the word
										&quot;run&quot; as though it were an obscenity. They're also
										quick to jokingly ridicule any hashers daring to wear T-shirts
										from road races. Such blasphemy will surely earn them a
										down-down at the <em>apres</em>. Other hash units are composed
										of many serious runners who thirst after physically daunting
										trails. They may even manage to create some kind of
										competitive twist to the event. Apparently, the primal forces
										of our human nature will cause the competitive fire to burn in
										contradiction of the hash mentality. Some hashers love to
										sing. The raunchier the lyrics, the better. And many hash
										units embrace the whole spectrum of motivations.
									</p>
									<p>
										But back to the individuals because here is where we have
										something special in the social interaction of all kinds of
										men and women. What is wonderful about it, and what is
										something of a unique hash phenomenon, is the total,
										unquestioning acceptance that hashers have for each other.
										People do not come to a hash with agendas that include a need
										to impress others with how important, or rich, or how smart
										they are. Nobody cares if you're a plumber, stockbroker, big
										shot executive, tax collector (well, that might create some
										negative disposition), lawyer (with a high tolerance for nasty
										jokes), salesperson, chemistry prof (they're the worst kind),
										or whatever. Criteria for acceptance into hash events are
										simply a few bucks to pay for food and drink, a love of
										adventure running on trails, and a zest for partying that is
										likely to be on the &quot;R-rated&quot; side (<em>ed</em> -
										see this month's Ask Dr. Down-Down column).
									</p>

									<p>With regard to attitudes and philosophies, the hash is
										typically a land of diversity: Conservatives and liberals,
										religious types, irreverent characters, party animals as well
										as quiet loners, and some people who drift in who are right
										off the wall. But these unique personalities are fully
										accepted and we don't call them &quot;weird.&quot; We prefer
										to say that they are just &quot;different,&quot; and the
										encompassing arms of the hash will be long enough and strong
										enough to encompass &quot;different&quot; people within our
										circle. One of the really delightful things about hashing is
										the chance it affords us to react to the smothering effect of
										political and social correctness. To be a rebel. To leave,
										temporarily, our sheltered structures and directed work-a-day
										worlds that are so filled with expectations and
										responsibilities. There are no Rules in the hash universe. The
										hash is the time and place for behavior based on a mock
										disrespect for genteel conventions and family values. But it's
										all done in a spirit of fun, and that's why it works and
										exists as a major part of the hash mystique. There's a lot of
										tongue-in-cheek insulting that goes on. A kind of crude banter
										that elicits smiles rather than hurt feelings. It is clearly
										understood that teasing is just a light-hearted bit of fluff
										among people who have profound respect and genuine affection
										for each other. It's the hash style to kid people about
										mismanaging events, laying ugly trails, or botching up
										whatever it is that somebody with a brain would do correctly.
										Another characteristic of many hash groups throughout the
										world is the special nickname that assembled hashers hang on a
										newly inducted member. The age, gender, or lifestyle of the
										newcomer is irrelevant. The scatological approach is basic to
										any flimsy pretext for selecting a raunchy name for the
										victim. Resisting an inclination to give some examples, it
										will be simply noted here that this singular event allows the
										hash to cross the line separating decency from the realm of
										poor taste and cruelty toward wildlife (i.e., hashers).</p>
									<p>Of course, the world of the hash has its share of human
										imperfection. Most of us will occasionally have that kind of
										day where we feel a little grouchy and behave in a way that
										rubs somebody's fur the wrong way. Where onr draws the line
										between good-natured taunting and crude insult varies with
										people. Some folks can handle an unrestricted litany of jokes
										and songs and always find the humor. But when you get into
										jokes involving sexual orientation, toilet functions, race,
										ethnicity, blondes, and lawyers, some people will feel deeply
										offended. What distinguishes humor from poor taste and gross
										insensitivity will always be perceived differently by
										different people. Personality conflicts are another
										inevitability, but that's something we'll always have to live
										with. What is great about the hash is the degree of harmony
										that seems to have become one of the major characteristics of
										our remarkably inclusive society.</p>
									<p>It is largely because of this spirit, this attitude, that
										the hash movement has evolved into an unstructured but
										nevertheless international affiliation. For example, it is
										absolutely fantastic how a hasher from one part of the world
										can get on the Internet and hit on the web pages of hashes
										thousands of miles away, then, choosing among the e-mail
										addresses displayed, contact an officer of any foreign hash,
										introduce himself and announce his plans to be there on such
										and such a date, and ask if there's a local hasher who has
										enough room for him to crash for a day or two so that he can
										hash with them. In the hash, the answer is never No, it's
										always positive. There is a real sense of fraternity among
										hashers throughout the world that opens doors and multiplies
										friendships.</p>

									<p>
										On-On!<br> Stan Cherim<br> Hockessin HHH, Deleware
										USA&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
									</p>

								<p align="center">
									<font face="Arial"><strong>Elements of a Hash</strong></font>
								</p>

								Basically a hash consists of three main parts, none of which
								have anything to do with the marijuana or hashish:
								<p>
									<strong>The Run (<em>aka</em> Trail)
									</strong>
								</p>
								<p>One or two hashers, called the hare(s), lay a trail. They
									mark their trail with chalk arrows, shredded paper, flour, or
									pieces of toilet paper hanging in the bush, depending on local
									tradition or terrain. They might pre-lay trail a day or a few
									hours before the hash, or they might lay the trail as
									&quot;live hares,&quot; running ahead of the pack with only a
									short (15 minutes is typical) head start. At a given signal,
									the rest of the hash (the Harriers, Harriettes, hounds, or
									pack) set off in pursuit of the trail. The idea is to keep the
									pack somewhat together and this is achieved by setting false
									trails, cunning checks, and sneaky loops. The fitter front
									runners will often run twice as far as the more slothful
									members, yet still finish the run at the same time as the rest
									of the pack. The length and difficulty of the run depends on
									the hare and the terrain but will typically be between four and
									eight kilometers, or about 45 minutes to an hour of running
									with checks, false trails, and shortcutting.</p>

								<p>
									<strong>The Circle (<em>aka</em> Religion)
									</strong>
								</p>
								<p>At trail's end hashers gather to drink beer and observe
									religious ceremonies . . . which consist of drinking more beer,
									this time ritualistically. Circles may be led by the hash
									Grandmaster, the Religious Adviser, or by a committee of
									mismanagement. Traditions (and the degree of rowdiness) vary
									from hash to hash, but in general the Circle consists of
									awarding &quot;Down-Downs&quot; for misdemeanors real,
									imagined, or blatantly made up, and the recipients will most
									likely have been dobbed in by their fellow hashers. Visitors
									are always given a Visitors Down-Down as are Virgins
									(first-time hash runners) and anyone else who comes to the
									attention of the Circle. The Circle can last a couple of
									minutes or half the night depending on the level of religious
									fervor of the hash. With changing times drinking has lost some
									of its importance and most clubs now modify their ceremonies to
									cater to non-drinkers and those stupid enough to think that
									hashing can improve their health.</p>
								<p>
									<strong>The On-On (<em>aka</em> On-On-On, On-Afters, or
										Apr\E8s)
									</strong>

								</p>
								<p>Some hashes suspend ceremonies for awhile to consume food
									provided by the hare(s). Other hashes, at the conclusion of the
									Circle, repair to a nearby restaurant or pub. This is the
									social part of the hash, and the party usually breaks up
									afterward. In some hashes, however, religion may continue
									during or after On-Ons, with the telling of jokes and singing
									of songs, and all members, visitors, and virgins should come
									armed with at least one joke or song lest they be called upon.
								</p>
